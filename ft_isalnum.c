#include <stdio.h>

int ft_isalnum(int x)
{
    if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z') || (x >= '0' && x <= '9'))
        return (1);
    return (0);
}

int main()
{
    int x = '+';

    printf("%d", ft_isalnum(x));
    return (0);
}